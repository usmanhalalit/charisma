            <h3>Built as a sprite</h3>

            <p>Instead of making every icon an extra request, we've compiled them into a sprite—a bunch of images in one
                file that uses CSS to position the images with <code>background-position</code>. This is the same method
                we use on Twitter.com and it has worked well for us.</p>

            <p>All icons classes are prefixed with <code>.icon-</code> for proper namespancing and scoping, much like
                our other components. This will help avoid conflicts with other tools.</p>
        </div>
        <div class="col-md-4">
            <h3>How to use</h3>

            <p>Bootstrap uses an <code>&lt;i&gt;</code> tag for all icons, but they have no case class—only a shared
                prefix. To use, place the following code just about anywhere:</p>
            <pre class="prettyprint linenums">&lt;i class="glyphicon glyphicon-search"&gt;&lt;/i&gt;</pre>
            <p>There are also styles available for inverted (white) icons, made ready with one extra class:</p>
            <pre class="prettyprint linenums">&lt;i class="glyphicon glyphicon-search icon-white"&gt;&lt;/i&gt;</pre>
            <div class="alert alert-info"><i class="glyphicon glyphicon-info-sign"></i> In dark themes (Cyborg and
                Slate) normal icons become white and icon-white become black.
            </div>
            <p>There are 140 classes to choose from for your icons. Just add an <code>&lt;i&gt;</code> tag with the
                right classes and you're set. You can find the full list in <strong>sprites.less</strong> or right here
                in this document.</p>

            <p>
                <span class="label-info label label-default">Heads up!</span>
                When using beside strings of text, as in buttons or nav links, be sure to leave a spance after the
                <code>&lt;i&gt;</code> tag for proper spancing.
            </p>
        </div>
        <div class="col-md-4">
            <h3>Use cases</h3>

            <p>Icons are great, but where would one use them? Here are a few ideas:</p>
            <ul>
                <li>As visuals for your sidebar navigation</li>
                <li>For a purely icon-driven navigation</li>
                <li>For buttons to help convey the meaning of an action</li>
                <li>With links to share context on a user's destination</li>
            </ul>
            <p>Essentially, anywhere you can put an <code>&lt;i&gt;</code> tag, you can put an icon.</p>
        </div>
    </div>

